# Arithmetic Operators
a = 7
b = 4
c = a + b
print(c)

# Assignment Operators
a = 4-2
b = 6
a += 3
print(a)
b-=3
print(b)

# Comparison Operators
d = 5<4
print(d) #False

# Logical Operators

e = True or False # Logical OR Operation
print(e)

f = True and False # Logical AND Operation
print(f)

print(not(f)) # Logical NOT Operation